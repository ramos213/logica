void main() {
  final numero1 = 10;
  final numero2 = 20;


  // soma (+)

  final resultadoSoma = numero1 + numero2;
  print("Soma: $resultadoSoma");

  // subtração (-)

  final resultadoSubtracao = 10 - 25;
  print("Subtração: $resultadoSubtracao");

  // multiplicacao (*)

  final resultadoMultiplicacao = 8 * 4;
  print("Multiplicação: $resultadoMultiplicacao");

  // divisão (/)

  final resultadoDivisao = 10 / 2;
  print("Divisão: $resultadoDivisao");

  // resto da divisão (%)
  final restoDivisao = 26 % 4;

  print("Resto da divisão: $restoDivisao");
}
